package ex5.utils;

public class VariableAssignmentPair {

    private String name;
    private String value;

    public VariableAssignmentPair(String name, String value) {
        this.name = name;
        this.value = value;
    }

    public String getName() {
        return name;
    }

    public String getValue() {
        return value;
    }
}
